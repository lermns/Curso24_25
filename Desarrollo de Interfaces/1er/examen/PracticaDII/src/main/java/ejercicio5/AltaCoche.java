    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        pr1 = new pruebaExamen();
        pr1.setAlt1(this);
        this.setVisible(false);
        int intColor = jSlider3.getValue();
        String color = intColor<100?"blanco":(intColor<180?"gris":"negro");
        String combustible;
        
        if(jRadioButton3.isSelected()){
            combustible="Gasolina";
        }else if(jRadioButton4.isSelected()){
            combustible="Diesel";
        }else if(jRadioButton5.isSelected()){
            combustible="Hibrido";
        }else{
            combustible="Electrico";
        }
        
        pr1.getModel_1().addRow(new Object[]{String.valueOf(jComboBox1.getSelectedItem()), jSpinner1.getValue(), jSlider2.getValue(), color, jRadioButton1.isSelected()?"Manual":"Automatico", combustible});
        pr1.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

